import java.util.ArrayList;
import java.util.List;

public class EvenOdd {
    public static void main(String[] args) {
        List<Integer> li = new ArrayList<>();
        for(int i=20;i<=50;i++){
            if (i%2==0) {
                li.add(i);
            
                
            }
        }
        System.out.println(li);
    }

}
